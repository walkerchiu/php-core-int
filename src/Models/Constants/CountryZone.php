    /**
     * @param Int     $from
     * @param Int     $to
     * @param String  $code
     * @return Array|String
     */
    public static function convertCode(int $from, int $to, string $code): array
    {
        $items = $this->listByUNCode();
        $items_new = [];
        foreach ($items as $item) {
            if (
                $from == 2
                && $to == 3
            ) {
                $items_new = array_merge($items_new, [$item[0] => $item[1]]);
            } elseif (
                $from == 3
                && $to == 2
            ) {
                $items_new = array_merge($items_new, [$item[1] => $item[0]]);
            } else {
                throw new InvalidArgumentException([$from, $to]);
            }
        }

        if (isset($items_new[$code])) {
            return $items_new[$$code];
        } else {
            throw new InvalidArgumentException($to);
        }
    }

