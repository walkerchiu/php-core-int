    /**
     * @param Int  $type
     * @return Array
     */
    public static function getCodes($type = 3): array
    {
        $items = [];
        $continents = self::all($type);
        foreach ($continents as $continent) {
            foreach ($continent as $code => $country) {
                array_push($items, $code);
            }
        }

        return $items;
    }

    /**
     * @param Int    $type
     * @param Array  $available
     * @return Array
     */
    public static function options($type = 3, $available = null): array
    {
        if (
            !is_null($available)
            && !is_array($available)
        ) {
            throw new InvalidArgumentException($available);
        }

        $items = [];
        $continents = self::all($type);
        foreach ($continents as $key => $continent) {
            $items = array_merge($items, [trans('php-core::area.'.$key) => []]);

            foreach ($continent as $code => $country) {
                if (
                    is_null($available)
                    || (
                        !empty($available)
                        && in_array($code, $available)
                    )
                ) {
                    $items[trans('php-core::area.'.$key)] = array_merge($items[trans('php-core::area.'.$key)], [$code => trans('php-core::area.'.$code)]);
                }
            }
        }

        return $items;
    }

